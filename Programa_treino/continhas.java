public class continhas{
    public static void main(String args[]){
        int a, b, r;

        a = 14;
        b = 3;

        r = a + b;
        System.out.println("Valor da soma = " + r);
        r = a - b;
        System.out.println("Valor da subt = " + r);
        r = a * b;
        System.out.println("Valor da mult = " + r);
        r = a / b;
        System.out.println("Valor da divi = " + r);
        r = a % b;
        System.out.println("Valor do resto= " + r);
        float x;
        x = 1.0f/2.0f;
        System.out.println("Valor do x = " + x);
    }
}